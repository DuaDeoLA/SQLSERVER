create table Category(
	CategoryID char(10) primary key,
	CategoryName varchar(50) NOT NULL
	)

go

create table Product(
	ProductID char(10) primary key,
	ProductName varchar(50) NOT NULL,
	selling_price int NOT NULL,
	CatagoryID char(10) NOT NULL,
	CONSTRAINT FK_Product FOREIGN KEY (CatagoryID)  REFERENCES Category(CategoryID)
)
go
create table Orders(
	OrderID int primary key,
	CustomerID char(10) NOT NULL,
	OrderDate datetime  NOT NULL,
	deliveryDate datetime  NOT NULL,
	CONSTRAINT FK_Rollno FOREIGN KEY (CustomerID)  REFERENCES Customer(CustomerID)

	)
go
create table OrderDetail(
	orderID int ,
	ProductID char(10),
	buying_qty int NOT NULL,
	CONSTRAINT PK_OrderDetail PRIMARY KEY (orderID, ProductID),
	CONSTRAINT FK_OrderDetail_1 FOREIGN KEY (orderID) REFERENCES Orders(orderID),
	CONSTRAINT FK_OrderDetail_2 FOREIGN KEY (ProductID) REFERENCES Product(ProductID),
)

